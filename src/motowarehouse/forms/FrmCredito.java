    public boolean formIsComplete() {
        
        boolean val = true;
    
     if (jDateChooser1.getCalendar() == null) {

            JOptionPane.showMessageDialog(null, "Registre una fecha de compra",
                    "Advertencia", TrayIcon.MessageType.WARNING.ordinal());
            val = false;
        }
             
        if(val && txtPrima.getText().trim() == "") {
            JOptionPane.showMessageDialog(null, "Registre la prima",
                    "Advertencia", TrayIcon.MessageType.WARNING.ordinal());
            val = false;
        }
        
        if(val && txtPrima1.getText().trim() == "") {
            JOptionPane.showMessageDialog(null, "Registre la couta",
                    "Advertencia", TrayIcon.MessageType.WARNING.ordinal());
            val = false;
        }
        
        if(val && txtInteres.getText().trim() == "") {
            JOptionPane.showMessageDialog(null, "Registre el interes",
                    "Advertencia", TrayIcon.MessageType.WARNING.ordinal());
            val = false;
        }
    
        return val;
    }
    
